# Algoritmo que determine si los datos perteneces a una mujer o un hombre

def ejercicio5():
    sexo = input("Ingrese su sexo (M/H): ")
    if sexo == "M":
        print("Sumerce es Mujer")
    elif sexo == "H":
        print("Sumerce es Hombre")
    else:
        print("Genero no Binario")
    
ejercicio5()